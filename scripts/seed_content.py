import requests

BASE = "http://localhost:8080/api"
TOKEN = None

def login():
    global TOKEN
    resp = requests.post(f"{BASE}/users/login", json={"account": "writer", "password": "writer123"})
    TOKEN = resp.json()["data"]["token"]
    print("登录成功")

def api(method, path, data=None):
    h = {"Content-Type": "application/json", "Authorization": f"Bearer {TOKEN}"}
    r = requests.request(method, f"{BASE}{path}", headers=h, json=data)
    if r.status_code >= 400:
        print(f"  ERROR {r.status_code}: {r.text[:200]}")
        return None
    return r.json().get("data", r.json())

def save_content(structure_id, text, status="DRAFT"):
    word_count = len(text.replace('\n','').replace(' ',''))
    return api("POST", f"/chapters/{structure_id}/content", {
        "content": text,
        "word_count": word_count,
        "source": "USER_EDITED",
        "storage_type": "FULL",
        "status": status,
    })

login()

